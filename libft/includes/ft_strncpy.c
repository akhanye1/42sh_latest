#include "../libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t i;
	size_t len;

	if (n <= 0)
		return (dest);
	i = 0;
	len = ft_strlen(src);
	while ((i < len) && (i < n))
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
