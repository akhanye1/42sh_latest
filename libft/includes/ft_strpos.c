#include <string.h>

int		ft_strpos(const char *s, int c)
{
	int		i;
	char	ch;

	i = 0;
	ch = (char)c;
	while ((s[i] != '\0') && (s[i] != ch))
		i++;
	if (s[i] == ch)
		return (i);
	return (-1);
}
