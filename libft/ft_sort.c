void	ft_sort(char *str)
{
	int		i;
	char	tmp;

	i = 1;
	while (str[i])
	{
		if (str[i] > str[i - 1])
		{
			tmp = str[i - 1];
			str[i - 1] = str[i];
			str[i] = tmp;
			i = 0;
		}
		i++;
	}
}
