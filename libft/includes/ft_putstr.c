#include "../libft.h"

void	ft_putstr(const char *str)
{
	int i;

	if (str == NULL)
	{
		ft_putstr("(null)");
		return ;
	}
	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}
