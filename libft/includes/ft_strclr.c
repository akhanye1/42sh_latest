#include "../libft.h"

void	ft_strclr(char *s)
{
	int len;
	int	i;

	if (s == NULL)
		return ;
	len = (int)ft_strlen(s);
	i = 0;
	while (i < len)
	{
		s[i] = '\0';
		i++;
	}
}
