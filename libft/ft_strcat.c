char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int x;

	x = 0;
	while (s1[x])
		x++;
	i = 0;
	while (s2[i])
	{
		s1[x] = s2[i];
		i++;
		x++;
	}
	s1[x] = '\0';
	return (s1);
}
