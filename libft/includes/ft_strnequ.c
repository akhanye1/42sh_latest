#include "../libft.h"

int	ft_strnequ(const char *s1, const char *s2, size_t n)
{
	size_t i;

	if (s1 == NULL || s2 == NULL)
		return (0);
	if (n == 0)
		return (1);
	i = 0;
	n--;
	while ((s1[i] == s2[i]) && (s1[i] != '\0') && (s2[i] != '\0') && (i < n))
	{
		i++;
	}
	if ((s1[i] == s2[i]) && (i <= n))
		return (1);
	return (0);
}
